$(function(){
    $(".collapse-on1").on("click",function(){
        if (!$(".collapse-main1").hasClass("show")){
            $("#collapse-main1").addClass("show");
            $("#collapse-main1").show();
            $(".collapse-on1").hide();
            $(".collapse-on2").hide();
            $(".collapse-on3").hide();
            $(".collapse-off").show();
        }else{
            $("#collapse-main1").removeClass("show");
            $("#collapse-main1").hide();
            $(".collapse-on2").show();
            $(".collapse-on3").show();
            }
        })
  })
